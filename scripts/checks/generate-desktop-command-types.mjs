import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";

import {
  desktopCommandGroups,
  desktopCommandNames,
} from "../../packages/types/src/desktop-ipc-commands.mjs";

const outputUrl = new URL(
  "../../packages/types/src/desktop-ipc-commands.d.mts",
  import.meta.url,
);

const domainNames = Object.keys(desktopCommandGroups);
const union = desktopCommandNames.map((name) => `  | ${JSON.stringify(name)}`).join("\n");
const content = `// Generated by scripts/checks/generate-desktop-command-types.mjs.\n`
  + `export type DesktopCommandName =\n${union};\n\n`
  + `export type DesktopCommandDomain = ${domainNames.map(JSON.stringify).join(" | ")};\n\n`
  + `export const desktopCommandGroups: Readonly<Record<DesktopCommandDomain, readonly DesktopCommandName[]>>;\n`
  + `export const desktopCommandNames: readonly DesktopCommandName[];\n`
  + `export function desktopCommandDomain(command: string): DesktopCommandDomain | null;\n`;

if (process.argv.includes("--check")) {
  const current = await readFile(outputUrl, "utf8").catch(() => "");
  if (current !== content) {
    throw new Error(`${fileURLToPath(outputUrl)} is stale; regenerate the Desktop command types.`);
  }
} else {
  await writeFile(outputUrl, content, "utf8");
}
